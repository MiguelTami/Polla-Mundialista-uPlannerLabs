create table if not exists public.user_knockout_predictions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  match_number integer not null check (match_number between 73 and 104 and match_number <> 103),
  home_team_id bigint not null references public.teams(id),
  away_team_id bigint not null references public.teams(id),
  predicted_home_score integer not null check (predicted_home_score between 0 and 99),
  predicted_away_score integer not null check (predicted_away_score between 0 and 99),
  predicted_winner_id bigint not null references public.teams(id),
  points_awarded integer not null default 0,
  updated_at timestamptz not null default now(),
  constraint knockout_distinct_teams check (home_team_id <> away_team_id),
  constraint knockout_winner_is_participant check (
    predicted_winner_id = home_team_id or predicted_winner_id = away_team_id
  ),
  unique (user_id, match_number)
);

alter table public.user_knockout_predictions enable row level security;

create policy "Users can read their own knockout predictions"
  on public.user_knockout_predictions
  for select
  to authenticated
  using ((select auth.uid()) = user_id);

create or replace function public.save_knockout_prediction(
  p_match_number integer,
  p_home_team_id bigint,
  p_away_team_id bigint,
  p_home_score integer,
  p_away_score integer,
  p_winner_id bigint
)
returns public.user_knockout_predictions
language plpgsql
security definer
set search_path = public, pg_catalog
as $$
declare
  current_user_id uuid := auth.uid();
  saved_prediction public.user_knockout_predictions;
begin
  if current_user_id is null then
    raise exception 'AUTH_REQUIRED' using errcode = '42501';
  end if;

  if now() >= timestamptz '2026-06-28 19:00:00+00' then
    raise exception 'BRACKET_LOCKED' using errcode = 'P0001';
  end if;

  if p_match_number not between 73 and 104 or p_match_number = 103 then
    raise exception 'INVALID_MATCH_NUMBER' using errcode = '22023';
  end if;

  if p_home_team_id is null or p_away_team_id is null
    or p_home_team_id = p_away_team_id
    or p_winner_id is null
    or p_winner_id not in (p_home_team_id, p_away_team_id)
    or p_home_score not between 0 and 99
    or p_away_score not between 0 and 99
  then
    raise exception 'INVALID_KNOCKOUT_PREDICTION' using errcode = '22023';
  end if;

  if p_home_score = p_away_score and p_winner_id is null then
    raise exception 'TIE_REQUIRES_WINNER' using errcode = '22023';
  end if;

  insert into public.user_knockout_predictions (
    user_id,
    match_number,
    home_team_id,
    away_team_id,
    predicted_home_score,
    predicted_away_score,
    predicted_winner_id,
    updated_at
  )
  values (
    current_user_id,
    p_match_number,
    p_home_team_id,
    p_away_team_id,
    p_home_score,
    p_away_score,
    p_winner_id,
    now()
  )
  on conflict (user_id, match_number) do update
  set
    home_team_id = excluded.home_team_id,
    away_team_id = excluded.away_team_id,
    predicted_home_score = excluded.predicted_home_score,
    predicted_away_score = excluded.predicted_away_score,
    predicted_winner_id = excluded.predicted_winner_id,
    points_awarded = 0,
    updated_at = now()
  returning *
  into saved_prediction;

  return saved_prediction;
end;
$$;

revoke all on function public.save_knockout_prediction(
  integer, bigint, bigint, integer, integer, bigint
) from public;

grant execute on function public.save_knockout_prediction(
  integer, bigint, bigint, integer, integer, bigint
) to authenticated;
